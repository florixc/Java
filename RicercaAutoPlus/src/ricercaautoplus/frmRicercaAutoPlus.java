    private void btnOrdinaABolleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdinaABolleActionPerformed
        //listaauto.sort(c);
        txtaStampa.setText("");
        int n = listaauto.size();
        for (int i = 0; i < n; i++) {
            for (int j = 1; j < (n - i); j++) {
                Automobile automobilep = (Automobile) listaauto.get(j - 1);
                Automobile automobiled = (Automobile) listaauto.get(j);
                //if (automobilep.getIdAutomobile() < automobiled.getIdAutomobile()) {
                int compa;
                compa = automobilep.getMarca().compareTo(automobiled.getMarca());
                if (compa == 0) {
                    compa = automobilep.getModello().compareTo(automobiled.getModello());
                }
                if (compa > 0) {
                    listaauto.set(j - 1, automobiled);
                    listaauto.set(j, automobilep);
                }
            }
        }
        stampa();
        indice = 0;
        elemento(indice);
    }//GEN-LAST:event_btnOrdinaABolleActionPerformed

