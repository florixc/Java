    private void btnEsportaXMLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEsportaXMLActionPerformed
        // TODO add your handling code here:

        Element ricercaauto = new Element("ricercaauto");
        for (int i = 0; i < listaauto.size(); i++) {
            Element automobile = new Element("automobile");
            Automobile auto = (Automobile) listaauto.get(i);

            Element idautomobile = new Element("idautomobile");
            idautomobile.appendChild(auto.getIdAutomobile() + "");
            Element modello = new Element("modello");
            modello.appendChild(auto.getModello());
            Element marca = new Element("marca");
            marca.appendChild(auto.getMarca());
            Element carrozzeria = new Element("carrozzeria");
            carrozzeria.appendChild(auto.getCarrozzeria());
            Element alimentazione = new Element("alimentazione");
            alimentazione.appendChild(auto.getAlimentazione());
            Element cilindrata = new Element("cilindrata");
            cilindrata.appendChild(auto.getCilindrata() + "");
            Element potenza = new Element("potenza");
            potenza.appendChild(auto.getPotenza() + "");
            Element prezzo = new Element("prezzo");
            prezzo.appendChild(auto.getPrezzo() + "");

            automobile.appendChild(idautomobile);
            automobile.appendChild(modello);
            automobile.appendChild(marca);
            automobile.appendChild(carrozzeria);
            automobile.appendChild(alimentazione);
            automobile.appendChild(cilindrata);
            automobile.appendChild(potenza);
            automobile.appendChild(prezzo);

            ricercaauto.appendChild(automobile);
        }
        Document doc = new Document(ricercaauto);
        txtaStampa.setText(doc.toXML());
    }//GEN-LAST:event_btnEsportaXMLActionPerformed

    private void btnImportaXMLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportaXMLActionPerformed
        Builder builder = new Builder();
        try {
            Document doc = builder.build(new StringReader(txtaStampa.getText()));
            txtaStampa.setText("");
            Element ricercaauto = doc.getRootElement();
            Elements automobili = ricercaauto.getChildElements("automobile");
            for (int i = 0; i < automobili.size(); i++) {
                Element automobile = automobili.get(i);
                Element idautomobile = automobile.getFirstChildElement("idautomobile");
                Element modello = automobile.getFirstChildElement("modello");
                Element marca = automobile.getFirstChildElement("marca");
                Element carrozzeria = automobile.getFirstChildElement("carrozzeria");
                Element alimentazione = automobile.getFirstChildElement("alimentazione");
                Element cilindrata = automobile.getFirstChildElement("cilindrata");
                Element potenza = automobile.getFirstChildElement("potenza");
                Element prezzo = automobile.getFirstChildElement("prezzo");

                listaauto.add(new Automobile(idautomobile, modello, marca, carrozzeria, alimentazione, cilindrata, potenza, prezzo));
                //listaauto.add(new Automobile(Integer.valueOf(idautomobile.getValue()), modello.getValue(), marca.getValue(), carrozzeria.getValue(), alimentazione.getValue(), Integer.valueOf(cilindrata.getValue()), Integer.valueOf(potenza.getValue()), Integer.valueOf(prezzo.getValue())));
            }
        } catch (ParsingException ex) {
            Logger.getLogger(frmRicercaAutoPlus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(frmRicercaAutoPlus.class.getName()).log(Level.SEVERE, null, ex);
        }
        stampa();
    }//GEN-LAST:event_btnImportaXMLActionPerformed

