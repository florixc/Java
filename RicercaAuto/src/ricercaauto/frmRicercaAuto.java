    private void bntInserisciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntInserisciActionPerformed

        //txtaStampa.setText("ciao \n");
        int idAutomobile = 0;
        String modello = null;
        String marca = null;
        String carrozzeria = null;
        String alimentazione = null;
        int cilindrata = 0;
        int potenza = 0;
        int prezzo = 0;
        try {
            idAutomobile = Integer.valueOf(txtIdAutomobile.getText());
            modello = txtModello.getText();
            marca = txtMarca.getText();
            carrozzeria = txtCarrozzeria.getText();
            alimentazione = txtAlimentazione.getText();
            cilindrata = Integer.valueOf(txtCilindrata.getText());
            potenza = Integer.valueOf(txtPotenza.getText());
            prezzo = Integer.valueOf(txtPrezzo.getText());
            Automobile automobile = new Automobile(idAutomobile, modello, marca, carrozzeria, alimentazione, cilindrata, potenza, prezzo);
            listaauto.add(automobile);
            //txtaStampa.append(automobile.toString() + "\n");
            txtaStampa.setText("inserito!!!" + automobile.toString() + "\n");
        } catch (NumberFormatException numberFormatException) {
            txtaStampa.setText("Errore nei dati!!!\n");
        }
        stampa();
    }//GEN-LAST:event_bntInserisciActionPerformed

    private void stampa() {
        for (int i = 0; i < listaauto.size(); i++) {
            Automobile automobile = (Automobile) listaauto.get(i);
            txtaStampa.append(automobile.toString() + "\n");
        }
    }

    private void bntStampaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntStampaActionPerformed
        txtaStampa.setText("");
        stampa();
    }//GEN-LAST:event_bntStampaActionPerformed

