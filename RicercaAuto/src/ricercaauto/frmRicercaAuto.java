    private void btnCercaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCercaActionPerformed
        txtaStampa.setText("");
        int idAutomobile = Integer.valueOf(txtIdAutomobile.getText());

        for (int i = 0; i < listaauto.size(); i++) {
            Automobile automobile = (Automobile) listaauto.get(i);
            if (automobile.getIdAutomobile() == idAutomobile) {
                txtaStampa.append("trovato: " + automobile.toString() + "\n");

                txtIdAutomobile.setText(automobile.getIdAutomobile() + "");
                txtModello.setText(automobile.getModello());
                txtMarca.setText(automobile.getMarca());
                txtCarrozzeria.setText(automobile.getCarrozzeria());
                txtAlimentazione.setText(automobile.getAlimentazione());
                txtCilindrata.setText(automobile.getCilindrata() + "");
                txtPotenza.setText(automobile.getPotenza() + "");
                txtPrezzo.setText(automobile.getPrezzo() + "");
            }
        }

// TODO add your handling code here:
    }//GEN-LAST:event_btnCercaActionPerformed

    private void btnCancellaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancellaActionPerformed
        txtaStampa.setText("");
        int idAutomobile = Integer.valueOf(txtIdAutomobile.getText());

        for (int i = listaauto.size() - 1; i >= 0; i--) {
            Automobile automobile = (Automobile) listaauto.get(i);
            if (automobile.getIdAutomobile() == idAutomobile) {
                listaauto.remove(i);
                txtaStampa.append("cancellato!!!" + automobile.toString() + "\n");
            }
        }
        stampa();
    }//GEN-LAST:event_btnCancellaActionPerformed

    private void btnModificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificaActionPerformed
        txtaStampa.setText("");
        int idAutomobile = Integer.valueOf(txtIdAutomobile.getText());

        for (int i = 0; i < listaauto.size(); i++) {
            Automobile automobile = (Automobile) listaauto.get(i);
            if (automobile.getIdAutomobile() == idAutomobile) {

                try {
                    automobile.setIdAutomobile(Integer.valueOf(txtIdAutomobile.getText()));
                    automobile.setModello(txtModello.getText());
                    automobile.setMarca(txtMarca.getText());
                    automobile.setCarrozzeria(txtCarrozzeria.getText());
                    automobile.setAlimentazione(txtAlimentazione.getText());
                    automobile.setCilindrata(Integer.valueOf(txtCilindrata.getText()));
                    automobile.setPotenza(Integer.valueOf(txtPotenza.getText()));
                    automobile.setPrezzo(Integer.valueOf(txtPrezzo.getText()));
                    txtaStampa.append("modificato!! " + automobile.toString() + "\n");
                } catch (NumberFormatException numberFormatException) {
                    txtaStampa.setText("Errore nei dati!!!\n");

                }

            }
        }
        stampa();
    }//GEN-LAST:event_btnModificaActionPerformed

